# Las variables es información que se almacena en la memoria del equipo.

#Definiendo una variable con camelCase
nombreCompletoDeTuTioMasterPython = "Lucas Dalto"

#Definiendo una variable con snake_case
nombre_completo_de_tu_tio_master_python = "Lucas Dalto"


#Concatenar con +
bienvenida = "Hola " + " ¿Cómo estás?"

#Concatenar con f-strings
bienvenida = f"Hola {nombre_completo_de_tu_tio_master_python}. ¿Cómo estás?"


#Operadores de pertenencia (in / not in)
print("Lucas" in bienvenida) #True
print("Lucas" not in bienvenida) #False